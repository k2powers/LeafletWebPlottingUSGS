console.log (API_quakes)
var API_plates = "https://raw.githubusercontent.com/fraxen/tectonicplates/master/GeoJSON/PB2002_boundaries.json"
console.log (API_plates)

function markerSize(magnitude) {
    return (magnitude*2) ^3;
};


var earthquakes = new L.LayerGroup();

d3.json(API_quakes, function (geoJson) {
    L.geoJSON(geoJson.features, {
        pointToLayer: function (geoJsonPoint, latlng) {
            return L.circleMarker(latlng, { radius: markerSize(geoJsonPoint.properties.mag) });
        },

        style: function (geoJsonFeature) {
            return {
                fillColor: Color(geoJsonFeature.properties.mag),
                fillOpacity: 0.8,
                weight: 0.5,
                color: 'red'

            }
        },

        onEachFeature: function (feature, layer) {
            layer.bindPopup(
                "<p>" + new Date(feature.properties.time) +"</p><p>" + feature.properties.title + "</p>");
        }
    }).addTo(earthquakes);
    createMap(earthquakes);
});

var tectonicPlates = new L.LayerGroup();

d3.json(API_plates, function (geoJson) {
    L.geoJSON(geoJson.features, {
        style: function (geoJsonFeature) {
            return {
                weight: 1,
                color: "brown"
            }
        },
    }).addTo(tectonicPlates);
})


function Color(magnitude) {
    if (magnitude > 5) {
        return 'darkred'
    } else if (magnitude > 4) {
        return 'red'
    } else if (magnitude > 3) {
        return 'darkorange'
    } else if (magnitude > 2) {
        return 'orange'
    } else if (magnitude > 1) {
        return 'yellow'
    } else {
        return 'lightyellow'
    }
};

